#include<stdio.h>
#define n 5
int stack[n];
int top=-1;
//--------------------------------------------------------------------------------------------------------
void push(){
int x;
printf("enter data to push in the stack:");
scanf("%d",&x);
if(top==n-1)
    printf("stack overflow\n");
else
   {top++;
    stack[top]=x;
   }
}
//------------------------------------------------------------------------------------------------------------------
void pop(){
if(top==-1)
    printf("stack underflowed\n");
else
   {top--;}
}
//-----------------------------------------------------------------------------------------------------------------------
void peek(){
  if(top==-1)
        printf("stack underflowed");
  else
  {printf("%d\n",stack[top]);}
}
//--------------------------------------------------------------------------------------------------------------------------------
void display(){
    int i;
    for(i=top;i>=0;i--)
        {printf("%d\n",stack[i] );
        }
}
//-------------------------------------------------------------------------------------------------------------------------------------
void main(){
    int ch;

    do{
        printf("enter choice");
    scanf("%d",&ch);
        switch(ch){
        case 1:
            {push();
            break;}
        case 2:
           {pop();
            break;}
        case 3:
           {peek();
            break;}
        case 4:
            {display();
            break;}
        default :
            {printf("invalid choice \n");
            }
        }
    }while(ch!=0);
}
